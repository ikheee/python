# 변수는 생성 될 때 숫자 값이 할당되기 때문에 
# Python은 이 변수를 숫자 변수로 처리합니다.
first_num = 6
second_num = 2

# 숫자 값(numeric value)에 대해 다양한 수치 연산 작업을 수행 할 수 있습니다.
print('addition')
print(first_num + second_num)
print('subtraction')
print(first_num - second_num)
print('multiplication')
print(first_num * second_num)
print('division')
print(first_num / second_num)
print ('exponent')
print(first_num ** second_num)
